from knock20 import get_article
import re

# 24. ファイル参照の抽出

uk = get_article("イギリス")
matches = re.findall(r'ファイル:(.+?)[\|\]]', uk)
print(matches)

# パターンの解説: r'ファイル:(.+?)[\|\]]'
# ファイル: → そのままの文字列にマッチ
# (.+?) → ファイル名を最短マッチで取り出す
# [\|\]] → |か]のどちらか一文字で止まる文字クラス

# |のみのパターンだと[[ファイル:example.png]]のような
# |がない形式に対応できない
# [\|\]]で両パターンに対応する

# 22のカテゴリ抽出と同じパターンの応用
# :の後ろから区切り文字までを最短マッチで取り出す設計は再利用できる