from knock20 import get_article
import re

# 23. セクション構造

uk = get_article("イギリス")
matches = re.findall(r'(={2,})\s*(.+?)\s*={2,}', uk)
for eq, name in matches:
    print(f"{name}: {len(eq)-1}")

# パターンの解説: r'(={2,})\s*(.+?)\s*={2,}'
# (={2,}) → =が2個以上の繰り返し。グループ1として取り出す
# \s* → 空白文字の0回以上。スペースを無視する
# (.+?) → セクション名を最短マッチで取り出す。グループ2
# \s* → 末尾の空白を無視
# ={2,} → 閉じ側の=。グループ不要なので()なし

# {2,}は2回以上の繰り返しを意味する量指定子
# {n}はn回、{n,m}はn回以上m回以下

# findall()で複数グループを指定するとタプルのリストを返す
# for eq, name in matchesでアンパックして受け取る

# len(eq)-1でレベルを計算する
# ==はlen=2でレベル1、===はlen=3でレベル2