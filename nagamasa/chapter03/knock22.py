from knock20 import get_article
import re

# 22. カテゴリ名の抽出

uk = get_article("イギリス")
matches = re.findall(r'Category:(.+?)[\|\]]', uk)
print(matches)

# re.findall(パターン, 文字列)は全マッチをリストで返す
# ()があるとグループの中身だけをリストで返す
# ()がないとマッチした全体をリストで返す

# re.search()は最初の一つだけマッチオブジェクトを返す
# re.findall()は全件リストで返す

# パターンの解説: r'Category:(.+?)[\|\]]'
# Category: → そのままの文字列
# (.+?) → 最短マッチで一文字以上を取り出す
# [\|\]] → |か]のどちらか一文字にマッチする文字クラス

# 最短マッチ(+?)と最長マッチ(+)の違い
# + はマッチできる限り長くマッチしようとする(最長マッチ)
# +? は最初に条件を満たした時点で止まる(最短マッチ)
# 区切り文字の手前で止めたい場合は+?を使う

# カテゴリ行には二パターンある
# [[Category:イギリス|*]]  → |で終わる
# [[Category:島国]]        → ]で終わる
# [\|\]]で両方に対応している